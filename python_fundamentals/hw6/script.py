# 3. Напишите программу, которая запрашивает у пользователя целое положительное число и проверяет, является ли оно простым.
# Простое число - это число, которое делится только на 1 и на само себя без остатка.
# Используйте цикл while и проверку деления числа на все числа от 2 до N-1 для решения задачи.
# Выведите соответствующее сообщение на экран с помощью команды print.
number = int(input('Введите целое положительное число: '))
delimiter = 2
while delimiter < number:
    if not number % delimiter:
        print(f'Число {number} не является простым.')
        break
    delimiter += 1
else:
    print(f'Число {number} является простым.')