# 1. Напишите программу, которая генерирует случайное число от 1 до 100, а затем предлагает пользователю угадать это число.
# Если пользователь угадывает число, программа выводит сообщение о победе. Если пользователь не угадывает число,
# программа сообщает, больше или меньше загаданное число и предлагает попробовать снова.
# Используйте цикл с инструкцией break, чтобы остановить выполнение цикла, когда число угадано.
import random

rand_number = random.randint(1, 100)

user_number = int(input('Угадайте число от 1 до 100: '))

while user_number != rand_number:
    print('Загаданное число', ('меньше' if user_number > rand_number else 'больше'))
    user_number = int(input('Попробуйте снова: '))

print(f'Поздравляю! Вы угадали число {rand_number}!')

